package com.example.findingshops.Data;

public class AuthenticBrands {

    private String name;
    private String description;

    public AuthenticBrands(String name, String description) {
        this.name = name;
        this.description = description;
    }

    public String getName() {
        return name;
    }
    public String getDescription() {
        return description;
    }
}
